// ============================================================
// ÉTAT GLOBAL DU MODE RAID
// ============================================================

let raidMode = false;

// ============================================================
// GETTERS
// ============================================================

function isRaidMode() {
    return raidMode;
}

// ============================================================
// ACTIONS
// ============================================================

function enableRaidMode() {
    raidMode = true;
}

function disableRaidMode() {
    raidMode = false;
}

// ============================================================
// EXPORTS
// ============================================================

module.exports = {
    isRaidMode,
    enableRaidMode,
    disableRaidMode,
};